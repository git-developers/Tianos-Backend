declare(strict_types=1);

namespace Bundle\UserBundle\Form\Model;

class PasswordResetRequest
{
    /**
     * @var string|null
     */
    private $email;

    /**
     * @return string|null
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string|null $email
     */
    public function setEmail(string $email)
    {
        $this->email = $email;
    }
}
